// Pre and Post Increment Exercise Test

// Post Increment
var x = 3;
var y = x++; // Post increment: 3 is assigned to y and then 1 is incremented to x
y += 1; // y = 4


// Pre Increment
var x = 3;
var y = ++x; // Post increment: 1 is incremented to x and then 4 (x+1) is assigned to y
y += 1; // y = 5

